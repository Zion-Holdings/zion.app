export async function getStaticProps() {
  const updatesDir = path.join(process.cwd(), 'pages', 'reports', 'updates');
  let updates: Array<{ slug: string; title: string; href: string }> = [];

  try {
    const files = fs.readdirSync(updatesDir)
      .filter((f) => f.endsWith('.tsx'))
      .sort((a, b) => (a < b ? 1 : -1));

    updates = files.slice(0, 12).map((file) => {
      const slug = file.replace(/\.tsx$/, '');
      const parts = slug.split('-');
      const human = parts.slice(1).join(': ').replace(/:/g, ': ');
      const title = `Autonomous Update — ${human}`;
      return {
        slug,
        title,
        href: `/reports/updates/${slug}`,
      };
    });
  } catch {
    updates = [];
  }

  return {
    props: { updates },
  };
}

