import React, { useState } from 'react';
import { Input } from '@/components/ui/input';
import { Textarea } from '@/components/ui/textarea';
import { logInfo } from '@/utils/productionLogger';
import {
  Select,
  SelectTrigger,
  SelectValue,
  SelectContent,
  SelectItem,
} from '@/components/ui/select';
import { Checkbox } from '@/components/ui/checkbox';
import { Button } from '@/components/ui/button';

export default function NationBuilder() {
  const [name, setName] = useState('');
  const [flag, setFlag] = useState<File | null>(null);
  const [constitution, setConstitution] = useState('');
  const [governance, setGovernance] = useState('admin');
  const [funding, setFunding] = useState<string[]>([]);

  const toggleFunding = (value: string) => {
    setFunding((prev) =>
      prev.includes(value) ? prev.filter((f) => f !== value) : [...prev, value]
    );
  };

  const handleGenerate = () => {
    setConstitution('Draft constitution generated by GPT (placeholder)');
  };

  const handleSubmit = (e: React.FormEvent) => {
    e.preventDefault();
    // Placeholder submit handler
    logInfo('Nation creation form submitted', { name, flag, constitution, governance, funding });
  };

  return (
    <div className="container mx-auto py-8 space-y-6">
      <h1 className="text-3xl font-bold text-white">Create a Digital Nation</h1>
      <form onSubmit={handleSubmit} className="space-y-4">
        <div>
          <label className="block mb-1">Nation Name</label>
          <Input value={name} onChange={(e) => setName(e.target.value)} />
        </div>

        <div>
          <label className="block mb-1">Flag</label>
          <Input
            type="file"
            accept="image/*"
            onChange={(e) => setFlag(e.target.files?.[0] || null)}
          />
        </div>

        <div>
          <label className="block mb-1">Constitution Statement</label>
          <Textarea
            className="min-h-[120px]"
            value={constitution}
            onChange={(e) => setConstitution(e.target.value)}
          />
          <Button type="button" variant="outline" className="mt-2" onClick={handleGenerate}>
            Generate with AI
          </Button>
        </div>

        <div>
          <label className="block mb-1">Governance Style</label>
          <Select value={governance} onValueChange={setGovernance}>
            <SelectTrigger className="w-full">
              <SelectValue placeholder="Select governance" />
            </SelectTrigger>
            <SelectContent>
              <SelectItem value="admin">Admin</SelectItem>
              <SelectItem value="token">Token-weighted vote</SelectItem>
              <SelectItem value="delegate">Delegate model</SelectItem>
            </SelectContent>
          </Select>
        </div>

        <div>
          <label className="block mb-2">Funding Model</label>
          <div className="flex flex-col gap-2">
            <label className="flex items-center gap-2">
              <Checkbox
                checked={funding.includes('treasury')}
                onCheckedChange={() => toggleFunding('treasury')}
              />
              <span>ZION$ treasury</span>
            </label>
            <label className="flex items-center gap-2">
              <Checkbox
                checked={funding.includes('subsidies')}
                onCheckedChange={() => toggleFunding('subsidies')}
              />
              <span>Partner subsidies</span>
            </label>
            <label className="flex items-center gap-2">
              <Checkbox
                checked={funding.includes('dues')}
                onCheckedChange={() => toggleFunding('dues')}
              />
              <span>Member dues</span>
            </label>
          </div>
        </div>

        <Button type="submit">Create Nation</Button>
      </form>
    </div>
  );
}
